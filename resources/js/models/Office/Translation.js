export default class Translation {

    static getTranslations(formData) {
        return new Promise((resolve, reject) => {
            axios.post('/api/translations', formData)
                .then(({data}) => {
                    resolve(data);
                })
                .catch((error) => {
                    reject(error);
                });
        });
    }

    static create(formData) {
        return new Promise((resolve, reject) => {
            axios.post('/api/translation/create', formData)
                .then(({data}) => {
                    resolve(data);
                })
                .catch((error) => {
                    reject(error);
                });
        });
    }

    static update(formData) {
        return new Promise((resolve, reject) => {
            axios.post('/api/translation/update', formData)
                .then(({data}) => {
                    resolve(data);
                })
                .catch((error) => {
                    reject(error);
                });
        });
    }

    static details(TranslationId) {
        return new Promise((resolve, reject) => {
            axios.post('/api/translation/details', {
                TranslationId: TranslationId
            })
                .then(({data}) => {
                    resolve(data);
                })
                .catch((error) => {
                    reject(error);
                });
        });
    }

    static delete(TranslationId) {
        return new Promise((resolve, reject) => {
            axios.post('/api/translation/delete', {
                TranslationId: TranslationId
            })
                .then(({data}) => {
                    resolve(data);
                })
                .catch((error) => {
                    reject(error);
                });
        });
    }
}
